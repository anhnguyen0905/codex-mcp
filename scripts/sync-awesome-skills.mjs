#!/usr/bin/env node
// Syncs the awesome-claude-skills list into a local REMOTE.md catalog — the
// foundation layer of the skill index. build-skills-index.mjs merges this
// catalog into INDEX.md; locally vetted skills always shadow remote entries.
//
// Usage:
//   node scripts/sync-awesome-skills.mjs [--from <url|file>] [--out <file>] [--clone] [--lib <dir>]
//
// Defaults:
//   from: https://raw.githubusercontent.com/travisvn/awesome-claude-skills/main/README.md
//   out:  <index dir>/REMOTE.md — i.e. ~/.claude/skill-library/REMOTE.md
//
// --clone (local-first mode): shallow-clones every GitHub repo in the list into
//   <lib>/remote/<owner>__<repo> (default lib: ~/claude-skill-library) and updates
//   existing clones. Non-GitHub entries stay as pointers in REMOTE.md. Rebuild the
//   index afterwards so the local copies shadow the remote pointers.
//
// Catalog format (one line per skill, same shape as INDEX.md):
//   <name> | <description> | <repo url>
//
// SECURITY: entries here are pointers, not installed skills. A remote skill's
// SKILL.md must be fetched and REVIEWED before it is saved into the local
// library — skills are prompt-injection surface for agents with write access.

import { execFile } from 'node:child_process'
import { promises as fs } from 'node:fs'
import os from 'node:os'
import path from 'node:path'
import { pathToFileURL } from 'node:url'
import { promisify } from 'node:util'

const execFileAsync = promisify(execFile)

const DEFAULT_SOURCE =
  'https://raw.githubusercontent.com/travisvn/awesome-claude-skills/main/README.md'

const defaultOut = () => path.join(os.homedir(), '.claude', 'skill-library', 'REMOTE.md')
const defaultLib = () => path.join(os.homedir(), 'claude-skill-library')

const sanitizeField = (text) => text.replace(/\s*\r?\n\s*/g, ' ').replace(/\|/g, '/').trim()

// Skill entries in the awesome list are bold links; plain links are articles/resources.
//   bullet: - **[name](url)** - description
//   table:  | **[name](url)** | description |
const BULLET_ENTRY = /^-\s+\*\*\[([^\]]+)\]\(([^)]+)\)\*\*\s*[-–—]?\s*(.*)$/
const TABLE_ENTRY = /^\|\s*\*\*\[([^\]]+)\]\(([^)]+)\)\*\*\s*\|\s*([^|]*)\|/

/** Parse the awesome-claude-skills README into { name, description, url } entries. */
export function parseAwesomeList(markdown) {
  const entries = []
  const seenUrls = new Set()

  for (const line of (markdown ?? '').split(/\r?\n/)) {
    const match = line.match(BULLET_ENTRY) ?? line.match(TABLE_ENTRY)
    if (!match) continue
    const [, name, url, description] = match
    if (seenUrls.has(url)) continue
    seenUrls.add(url)
    entries.push({
      name: sanitizeField(name),
      description: sanitizeField(description) || '(no description)',
      url: url.trim(),
    })
  }
  return entries
}

/** Render entries as the REMOTE.md catalog content (same line shape as INDEX.md). */
export function renderCatalog(entries) {
  const header = [
    '# Remote skill catalog — generated by codex-flow sync-awesome-skills',
    `# Synced: ${new Date().toISOString()}`,
    '# Entries are POINTERS: fetch + review a SKILL.md before saving it locally.',
    '# Format: <name> | <description> | <repo url>',
    '',
  ]
  const lines = entries.map((e) => `${e.name} | ${e.description} | ${e.url}`)
  return [...header, ...lines, ''].join('\n')
}

/** GitHub URL → { cloneUrl, dir } (repo root, `owner__repo` dir), or null for non-GitHub. */
export function parseRepoTarget(url) {
  const match = url.match(/^https:\/\/github\.com\/([^/]+)\/([^/#?]+)/)
  if (!match) return null
  const [, owner, repo] = match
  return {
    cloneUrl: `https://github.com/${owner}/${repo}.git`,
    dir: `${owner}__${repo}`,
  }
}

/** Split entries into unique clone targets (GitHub) and pointer-only entries (everything else). */
export function collectCloneTargets(entries) {
  const targets = []
  const pointers = []
  const seenDirs = new Set()

  for (const entry of entries) {
    const target = parseRepoTarget(entry.url)
    if (!target) {
      pointers.push(entry)
      continue
    }
    if (seenDirs.has(target.dir)) continue
    seenDirs.add(target.dir)
    targets.push(target)
  }
  return { targets, pointers }
}

/**
 * Shallow-clone new targets into destDir and fast-forward existing ones.
 * Per-repo failures are collected, never abort the batch.
 */
export async function cloneRepos(targets, destDir) {
  const result = { cloned: 0, updated: 0, failed: [] }
  await fs.mkdir(destDir, { recursive: true })

  for (const { cloneUrl, dir } of targets) {
    const dest = path.join(destDir, dir)
    const exists = await fs
      .stat(path.join(dest, '.git'))
      .then((s) => s.isDirectory())
      .catch(() => false)
    try {
      if (exists) {
        await execFileAsync('git', ['-C', dest, 'pull', '--ff-only', '--quiet'])
        result.updated++
      } else {
        await execFileAsync('git', ['clone', '--depth', '1', '--quiet', cloneUrl, dest])
        result.cloned++
      }
    } catch (error) {
      result.failed.push(`${dir}: ${error.message.split('\n')[0]}`)
    }
  }
  return result
}

async function readSource(from) {
  if (/^https?:\/\//.test(from)) {
    const response = await fetch(from)
    if (!response.ok) throw new Error(`fetch failed (${response.status}) for ${from}`)
    return response.text()
  }
  return fs.readFile(path.resolve(from), 'utf8')
}

/**
 * Parse argv, fetch/read the list, write the catalog — and in --clone mode,
 * shallow-clone every GitHub repo into <lib>/remote/. Returns
 * { count, out, from, clone? } where clone = { cloned, updated, failed, lib }.
 */
export async function runCli(argv) {
  let from = DEFAULT_SOURCE
  let out = null
  let lib = null
  let clone = false

  for (let i = 0; i < argv.length; i++) {
    const arg = argv[i]
    if (arg === '--from' || arg === '-f') {
      from = argv[++i]
      if (!from) throw new Error(`${arg} requires a url or file path`)
    } else if (arg === '--out' || arg === '-o') {
      out = argv[++i]
      if (!out) throw new Error(`${arg} requires a file path`)
    } else if (arg === '--lib' || arg === '-l') {
      lib = argv[++i]
      if (!lib) throw new Error(`${arg} requires a directory path`)
    } else if (arg === '--clone' || arg === '-c') {
      clone = true
    } else {
      throw new Error(`unknown argument: ${arg}`)
    }
  }

  const markdown = await readSource(from)
  const entries = parseAwesomeList(markdown)
  if (entries.length === 0) {
    throw new Error(`no skill entries found in ${from} — has the list format changed?`)
  }

  const outFile = path.resolve(out ?? defaultOut())
  await fs.mkdir(path.dirname(outFile), { recursive: true })
  await fs.writeFile(outFile, renderCatalog(entries), 'utf8')
  const summary = { count: entries.length, out: outFile, from }

  if (clone) {
    const libDir = path.resolve(lib ?? defaultLib())
    const { targets } = collectCloneTargets(entries)
    const cloneResult = await cloneRepos(targets, path.join(libDir, 'remote'))
    summary.clone = { ...cloneResult, lib: libDir, repos: targets.length }
  }

  return summary
}

const isDirectRun =
  process.argv[1] && import.meta.url === pathToFileURL(path.resolve(process.argv[1])).href

if (isDirectRun) {
  runCli(process.argv.slice(2))
    .then(({ count, out, from, clone }) => {
      console.log(`Synced ${count} remote skill(s) from ${from}`)
      console.log(`→ ${out}`)
      if (clone) {
        console.log(
          `Cloned ${clone.cloned}, updated ${clone.updated} of ${clone.repos} repo(s) → ${path.join(clone.lib, 'remote')}`,
        )
        for (const failure of clone.failed) console.warn(`⚠ ${failure}`)
      }
      console.log('Rebuild the index to include them: node scripts/build-skills-index.mjs')
    })
    .catch((error) => {
      console.error(`sync-awesome-skills: ${error.message}`)
      process.exit(1)
    })
}
